#include "libft.h"

char	*ft_strdup(const char *str)
{
	char			*ptr;
	unsigned int	i;

	ptr = (char*)malloc(sizeof(char) * ft_strlen(str) + 1);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (str[i])
	{
		ptr[i] = str[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
